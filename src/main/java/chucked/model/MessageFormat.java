package chucked.model;

public class MessageFormat {
	
	
	private String message;
	private String messgaid;
	private int chucknumber;
	private int Totalchuck;
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	public String getMessgaid() {
		return messgaid;
	}
	public void setMessgaid(String messgaid) {
		this.messgaid = messgaid;
	}
	public int getChucknumber() {
		return chucknumber;
	}
	public void setChucknumber(int chucknumber) {
		this.chucknumber = chucknumber;
	}
	public int getTotalchuck() {
		return Totalchuck;
	}
	public void setTotalchuck(int totalchuck) {
		Totalchuck = totalchuck;
	}
	
	

}
